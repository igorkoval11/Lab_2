"""
A2 — Можно ли привести к int(...) за одну операцию?
Скрипт печатает для каждого примера вердикт и пояснение (без вычисления опасных выражений).
"""
cases = [
    ("'123е'",  "NO  ", "некорректный десятичный литерал (ещё и кириллическая 'е')"),
    ("'91.4'",  "NO  ", "содержит точку; нужно float(...) -> int(...), это уже две операции"),
    ("524.345 ** 435345345311145345", "NO  ", "переполнение/ошибка ещё на этапе возведения в степень"),
    ("'7.1 + 4'", "NO  ", "это строка-выражение, а не число"),
    ("'4' - 2",   "NO  ", "TypeError — вычитание строки и числа"),
    ("'4 - 2'",   "NO  ", "строка-выражение, не число"),
    ("'42'",      "YES ", "int('42') == 42"),
    ("-12.12",    "YES ", "int(-12.12) == -12 (усечение к нулю)"),
]

if __name__ == "__main__":
    for expr, verdict, note in cases:
        print(f"{expr:40s} : {verdict} | {note}")
